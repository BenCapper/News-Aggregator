    def __init__(
        self,
        headline,
        date,
        image,
        image_title,
        link,
        outlet,
        storage_link,
    ):
        self.headline = headline
        self.date = date
        self.image = image
        self.image_title = image_title
        self.link = link
        self.outlet = outlet
        self.storage_link = storage_link
